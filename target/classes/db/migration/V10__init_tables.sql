CREATE TABLE AUTHORS (id NUMBER PRIMARY KEY, name VARCHAR2(100));
CREATE TABLE PUBLISHERS (id NUMBER PRIMARY KEY, name VARCHAR2(100));
CREATE TABLE BOOKS (
    id NUMBER PRIMARY KEY, 
    title VARCHAR2(200), 
    author_id NUMBER REFERENCES AUTHORS(id), 
    publisher_id NUMBER REFERENCES PUBLISHERS(id)
);
CREATE TABLE AUDIT_LOGS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    book_id NUMBER, 
    action_date TIMESTAMP, 
    action_user VARCHAR2(50)
);

CREATE OR REPLACE TRIGGER books_audit_trg
AFTER INSERT ON BOOKS
FOR EACH ROW
BEGIN
    INSERT INTO AUDIT_LOGS (book_id, action_date, action_user)
    VALUES (:new.id, SYSTIMESTAMP, USER);
END;
